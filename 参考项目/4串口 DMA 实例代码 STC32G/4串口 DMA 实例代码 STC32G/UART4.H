//主要收发函数使用说明:
/**
  * @功能  串口发送数据. 写数据至发送缓冲区(循环池), 写完立即返回, 由DMA管理数据流向串口, 用户无须关心. 只要缓冲区有足够的空间, 可持续写入数据
  * @参数  pt: 发送数据指针
  * @参数  Size: 发送数量(字节)
  * @返回值 当缓冲区没有足够的空间装入数据时返回1, 其它时候返回0
  * 原型: u8 UART4_Send( void *pt, u16 Size);
  */
  
  
/**
  * @功能  读串口数据, 从缓冲区内读取数据. (DMA接收数据后存放至接收缓冲区, 应用代码必须定时查询读取, 否则循环池发生数据覆盖, 会丢失一部分数据, 没有提示, 但不影响后续收发)
  * @参数  buf: 接收数据指针
  * @参数  Size: Size期盼接收的字节数
  * @返回值 实际接收字节数. 缓冲区空时(没有数据可读)返回0, 返回值<Size说明当读取完成后,缓冲区已空. 返回值==Size说明当读取完成后,缓冲区仍有数据可读. 任何时候,返回值不会大于Size
  * 原型: u16 UART4_Receive(u8 *buf, u16 Size);
  */

#ifndef __UART4_H_
#define __UART4_H_

#include "Config.H"


/*---------------------------------------------------------
设置
----------------------------------------------------------*/
#define UART4_SEND_BUF_SIZE     256     //在此设置数据池大小
#define UART4_RECEIVE_BUF_SIZE  256     //在此设置数据池大小



/*---------------------------------------------------------
初始化
----------------------------------------------------------*/
void UART4_Init(u32 btl);               //初始化串口            

u8 UART4_Send(void *pt, u16 Size);      //发送数据

u16 UART4_Receive(u8 *buf, u16 Size);   //接收数据

u16 UART4_GetCapacity( void );          //返回发送缓冲区剩余空间




#endif



