#include <Seg.h>

unsigned char Dula[] = {0xc0,0xf9,0xa4,0xb0,0x99,0x92,0x82,0xf8,0x80,0x90,0xff,0xbf};
unsigned char Wela[]= {0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80};

void Seg_Disp(unsigned char wela,unsigned char dula,unsigned char point)
{
	//消隐
	P0 = 0xff;
	P2 = P2 & 0x1f | 0xe0;
	P2 = P2 & 0x1f;
	
	//位选
	P0 = Wela[wela];
	P2 = P2 & 0x1f | 0xc0;
	P2 = P2 & 0x1f;
	
	//段选
	P0 = Dula[dula];
	if(point) P0 &= 0x7f;
	P2 = P2 & 0x1f | 0xe0;
	P2 = P2 & 0x1f;
}